package org.digital_academy.user;

import jakarta.persistence.*;

import java.util.Set;



@Entity
@Table(name = "users") // ðŸ‘ˆ importante no chocar con "user" reservado en SQL
public class UserEntity {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(unique = true, nullable = false)
    private String username;

    private String password;

    @ElementCollection(fetch = FetchType.EAGER)
    @CollectionTable(name = "user_roles", joinColumns = @JoinColumn(name = "user_id"))
    @Column(name = "role")
    private Set<String> roles; // ðŸ‘ˆ ["ADMIN", "USER", "RECEPCIONISTA"]    private Set<String> roles; // ðŸ‘ˆ ["ADMIN", "USER", "RECEPCIONISTA"]

    public UserEntity() {
    }

    public UserEntity(Long id, String username, String password, Set<String> roles) {
        this.id = id;
        this.username = username;
        this.password = password;
        this.roles = roles;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Set<String> getRoles() {
        return roles;
    }

    public void setRoles(Set<String> roles) {
        this.roles = roles;
    }
}